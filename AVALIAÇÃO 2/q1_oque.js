/*O QUE É, E PARA QUE É?*/

/*LETRA A) O pensamento computacional,diferentemente do que seu nome sugere, não está relacionado, apenas, à programação
de computadores ou navegação na internet. Ele está baseado em uma sistematização de ideias afim de alcançar a solução de problemas
de forma mais eficaz, utilizando-se dos quatro pilares: decomposição, reconhecimento de padrões, abstração e algoritmos.*/


/*LETRA B) Algoritmos são o conjuntos de regras e procedimentos lógicos que levam à solução de um problema em um número finito de etapas.*/

/*LETRA C) Programação é um conjunto de processos de escritas (utilizando linguagem apropriada), testes e manutenção de computadores.
Extremamente ultilizada nos dias de hoje, onde a tecnologia se tornou fundamental, a programação vai além do uso pessoal, mas também em aréas
como medicina, com seus aparelhos de monitoração remota; na arquiterura e engenharia, através de seus diversos softwares para criação de
projetos. Entre diversos outros exemplos, a programação, hoje, já faz parte da vida de toda a sociedade.*/

/*LETRA D) Modulo é um pedaço de um código que é representado de forma isolada do restante da função e que pode ser utilizado em outros
códigos, quando solicitado */
